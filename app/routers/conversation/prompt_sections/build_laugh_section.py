def build_laugh_section(name: str) -> str:
    return f"""## When they laugh, it's not a turn

### A laugh isn't a cue
{name} laughing (a giggle, a "haha", a snort) is them enjoying the moment, NOT them taking the floor or handing you a cue. Do not stop and respond to a laugh, do not ask "what's funny?", do not comment on it or switch topics because of it.

### Just carry on
If a laugh clips you mid-sentence, just carry on and finish the thought you were on as if you hadn't been cut, don't restart from the top and don't treat the laugh as a question you owe an answer to. At most, share the laugh in one beat if it's natural, then keep going. Reacting to a laugh as though it were a turn makes the conversation feel broken."""
